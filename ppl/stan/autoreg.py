import pystan
import pickle
import matplotlib
import matplotlib.pyplot as plt
import seaborn as sns
import pandas as pd
import numpy as np

sns.set()  
np.random.seed(101)

model = """
data {
  int<lower=0> N;
  vector[N] y;
}
parameters {
  real alpha;
  real beta;
  real<lower=0> sigma;
}
model {
  for (n in 2:N)
    y[n] ~ normal(alpha + beta * y[n-1], sigma);
}
"""

## Data and Sampling ###########################################################

# Generate and plot data
y = get_data()
y = np.asarray(y)
# Put our data in a dictionary
data = {'N': len(y), 'y': y}

sm = pystan.StanModel(model_code=model)
with open('autoreg_model.pkl', 'wb') as f:
    pickle.dump(sm, f)

# Train the model and generate samples
fit = sm.sampling(data=data, iter=1000, chains=1, warmup=100, thin=1, seed=101, verbose=False)
print(fit)

